<p>
Voici les premières étapes pour vous lancer dans l'aventure.<br/>
</p>
<br/>

<h3>1. Vous connecter</h3>
<p>
Utilisez vos informations d'authentification Groupinfra pour <a href="<?php echo site_url('auth/login'); ?>">vous connecter</a> sur le site.
</p>
<br/>

<h3>2. Télécharger un kit de démarrage</h3>
<p>
Télécharger un des kits de démarrage disponible <a href="<?php echo site_url('game/kits'); ?>">ici</a>.<br/>
<br/>
A ce stade, peut importe le kit de démarrage que vous choisissez, car nous ne le modifierons pas encore.<br/>
Choisissez n'importe quel kit de démarrage, et téléchargez le.
Les kits de démarrage sont tous des fichiers ZIP, et vous devriez maintenant avoir l'un de ces fichiers ZIP sur votre ordinateur.
</p>
<br/>

<h3>3. Soumettre le Kit</h3>
<p>
Ne vous embêtez pas décompresser ou de modifier le kit de démarrage vous venez de télécharger.<br/>
Assurez-vous que vous êtes <a href="<?php echo site_url('auth/login'); ?>">connecté</a> au site web, 
allez sur la <a href="<?php echo site_url('user/bot_upload'); ?>">page d'upload</a> et 
soumettez le kit de démarrage (fichier ZIP) que vous venez de télécharger.
</p>
<br/>

<h3>4. Vous avez fini</h3>
<p>
Depuis que vous avez soumis une entrée, votre nom apparaîtra bientôt dans le classement mondial. 
Cela peut prendre jusqu'à une heure pour que votre nom apparaisse, mais ne prend généralement que quelques minutes.
</p>
<br/>

