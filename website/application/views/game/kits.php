Voici les kits de démarrage que nous offrons actuellement.<br/>
Chaque kit de démarrage est un fichier ZIP qui contient:
<ul>
	<li>Une entrée déjà fonctionnelle simple à utiliser comme point de départ</li>
	<li>Des outils qui vous permettent de gérer votre IA et de le regarder jouer graphiquement</li>
	<li>Certains échantillons d'opposants que vous pouvez tester contre votre IA</li>
	<li>Des cartes générées aléatoirement que vous pouvez utiliser pour tester</li>
</ul>
<br/>
<p>
Étant donné que chaque kit de démarrage contient une entrée déjà fonctionnelle, 
vous pouvez directement envoyer le fichier ZIP sur la page d'upload.<br/>
Voir le guide de <a href="<?php echo site_url('game/start') ?>">Démarrage Rapide</a> pour 
apprendre comment obtenir votre nom sur le leaderboard mondial en moins de cinq minutes.<br/>
</p>
